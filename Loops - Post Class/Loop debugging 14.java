// Given an integer N, your task is to print all the even integer from 1 to N.


public static void For_Loop(int n){
        for(int i=2;i<=n;i++){
            System.out.print(i+" ");
            i++;
        }
    }

// Sample Input:-
// 5

// Sample Output:-
// 2 4

// Sample Input:-
// 6

// Sample Output:-
// 2 4 6
